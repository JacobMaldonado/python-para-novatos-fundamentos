# Tipo de dato texto
variable_texto = "Esto es un texto"

print(variable_texto)
print(type(variable_texto))

# Tipo de dato Número (entero)
variable_texto = 10

print(variable_texto)
print(type(variable_texto))

# Tipo de dato Número (decimal)
variable_texto = 10.5

print(variable_texto)
print(type(variable_texto))

# Tipo de dato booleanos
variable_texto = False 

print(variable_texto)
print(type(variable_texto))